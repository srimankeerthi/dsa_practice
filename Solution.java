import java.util.*;
class Solution{
    public static void main(String a[]){
            Scanner sc = new Scanner(System.in);
            int n=sc.nextInt();
            int[] arr= new int[n];
            
            for(int i=0;i<n;i++){
                arr[i] = sc.nextInt();
            
            for(int j=0;j<n;j++){
               if (i % 2 == 0) {  
                 System.out.println(arr[i]+ " ");
                }
                }
            }
        }
                
                
}
